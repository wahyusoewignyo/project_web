     <h1 align="center"> Kemenhan Apresiasi Teknologi Antisadap Buatan Indonesia </h1>
         <p align="justify"> REPUBLIKA.CO.ID, JAKARTA -- Kasus penyadapan bukan hal baru di Indonesia. Tidak hanya pejabat publik, kasus penyadapan juga terjadi pada pengusaha dan warga negara Indonesia. Salah satu kasus paling menghebohkan adalah penyadapan Australia terhadap Presiden Susilo Bambang Yudhoyono di tahun 2014.</br>

Selain di Indonesia, kasus penyadapan juga ramai menjadi perbincangan dunia. Seperti yang dibeberkan dokumen Wikileaks puluhan kepala negara telah menjadi korban penyadapan. Bahkan, akhir-akhir ini email direktur CIA berhasil diretas oleh seorang pelajar SMA di Amerika Serikat.
Belajar dari kasus penyadapan yang membobol pertahanan negara, maka upaya pencegahan dan penangkalan perlu dilakukan. Satu-satunya cara adalah dengan teknologi enkripsi. Di Indonesia, sudah berdiri perusahaan pengembang teknologi antisadap pertama bernama PT Indoguardika Cipta Kreasi (ICK).</br>

"Teknologi enkripsi adalah satu-satunya cara melindungi diri dari penyadapan. Itulah teknologi antisadap paling canggih saat ini," ujar President Director PT ICK Agung S Bakti dalam jumpa pers di Jakarta, Jumat (30/10). Agung didampingi Research and Production Director Sujoko dan Technology Director Dahniar Paramita.</br>

Teknologi enkripsi, lanjut Agung, akan memproses informasi dan komunikasi dengan algoritma tertentu. Data akan diacak sehingga hanya bisa dibaca pihak yang dituju yang mempunyai kuncinya.</br>

Pada dasarnya, kata Agung, semua bentuk komunikasi yang ditransmisikan baik lewat kabel, internet, gelombang radio bahkan satelit itu bisa disadap. Namun dengan teknologi enkripsi, data atau komunikasi tersebut tidak bisa dibaca si penyadap. “Data yang dienkrip akan terlindungi dalam jutaan atau miliaran kode-kode acak yang sangat sulit dipecahkan,” jelas Agung.

Untuk meminimalisasi dan menjaga informasi sensitif dari sadapan asing dan pihak yang tidak bertanggungjawab, maka teknologi enkripsi buatan dalam negeri diperlukan. PT ICK hadir untuk memenuhi kebutuhan pertahanan bidang pengamanan informasi teknologi tersebut. </p>
